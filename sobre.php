	  <div><br><div class="cl">&nbsp;</div><br><h1 class="documentFirstHeading">A Faculdade</h1>
<p style="text-align: justify;">Fundada em 08 de maio de 2000, nasceu da iniciativa de contribuir para a forma&ccedil;&atilde;o de profissionais e especialistas nas diferentes &aacute;reas de conhecimento, oferecendo cursos com diferencial que agreguem din&acirc;mica ao mercado de trabalho e atendendam &agrave;s necessidades da sociedade.</p>
<p style="text-align: justify;">Com essa vis&atilde;o, o mantenedor de outras institui&ccedil;&otilde;es educacionais, Jos&eacute; Fernando Pinto da Costa, vislumbrou em Presidente Prudente um p&oacute;lo capaz de absorver o fortalecimento educacional. Em parceria com os empres&aacute;rios S&eacute;rgio do Nascimento Silva, Danilo Nascimento Silva, Silvio Camarini e Aparecida Maria Clapis de Paula constitu&iacute;ram o grupo de mantenedores que buscam ampliar seus horizontes atrav&eacute;s de novos desafios e conquistas. Come&ccedil;a assim a hist&oacute;ria da IESPP, que iniciou as suas atividades com o curso MBA Executivo em outubro de 2000, sob a coordena&ccedil;&atilde;o do prof. e Dr. Takeshy Tachizawa (USP).</p>
<p style="text-align: justify;">Em maio de 2001 foi autorizada a Faculdade de Presidente Prudente-FAPEPE, mantida pela IESPP, criada para proporcionar a forma&ccedil;&atilde;o profissional com atendimento &agrave;s demandas do mercado de trabalho, objetivando um conjunto de princ&iacute;pios em dire&ccedil;&atilde;o &agrave; qualidade do ensino superior; hoje denominada UNIESP - Faculdade de Presidente Prudente.</p>
<p style="text-align: justify;">A Faculdade de Presidente Prudente tem por objetivo constante o aprimoramento da qualidade de ensino procura, atrav&eacute;s de iniciativas para incrementar sua infra-estrutura e seus espa&ccedil;os f&iacute;sicos, privilegiar o aprendizado e o fortalecimento de uma metodologia consolidada na viv&ecirc;ncia profissional, que busca habilitar o acad&ecirc;mico para novos desafios e conquistas em suas carreiras.</p>

