	  <div><br><div class="cl">&nbsp;</div><br><h1 class="documentFirstHeading">Acessibilidade</h1>
<div id="parent-fieldname-text"><p><strong>A Faculdade De Hortolandia</strong> atende às principais recomendações de acessibilidade indicadas para web.</p>
<p>O termo acessibilidade significa incluir a pessoa com deficiência na participação de atividades como o uso de produtos, serviços e informações. Alguns exemplos são os prédios com rampas de acesso para cadeira de rodas e banheiros adaptados para pessoas com deficiência.</p>
<p>Na internet, acessibilidade refere-se principalmente às recomendações do WCAG (World Content Accessibility Guide) do W3C e no caso do Governo Brasileiro ao e-MAG (Modelo de Acessibilidade em Governo Eletrônico). O e-MAG está alinhado as recomendações internacionais, mas estabelece padrões de comportamento acessível para sites governamentais.</p>
<p>Na parte superior do portal da Unimep existe uma barra de acessibilidade onde se encontra atalhos de navegação padronizados e a opção para alterar o contraste. Essas ferramentas estão disponíveis em todas as páginas do portal.</p>
<p>Os nossos atalhos são:</p>
<p>Teclando-se Alt + 1 em qualquer página do portal, chega-se diretamente ao começo do conteúdo principal da página.</p>
<p>Teclando-se Alt + 2 em qualquer página do portal, chega-se diretamente ao início do menu lateral.</p>
<p>Teclando-se Alt + 3 em qualquer página do portal, chega-se diretamente em sua busca interna.</p>
<p>Teclando-se Alt + 4 em qualquer página do portal, chega-se diretamente ao início do menu superior.</p>
<p>Teclando-se Alt + 5 em qualquer página do portal, chega-se diretamente ao início do rodapé.</p>
<p>No caso do Firefox, em vez de Alt + número, tecle simultaneamente Alt + Shift + número.</p>
<p>Sendo Firefox no Mac OS, em vez de Alt + Shift + número, tecle simultaneamente Ctrl + Alt + número.</p>
<p>No Opera, as teclas são Shift + Escape + número. Ao teclar apenas Shift + Escape, o usuário encontrará uma janela com todas as alternativas de ACCESSKEY da página.</p>
<p>Ao final desse texto, você poderá baixar alguns arquivos que explicam melhor o termo acessibilidade e como deve ser implementado nos sites da Internet.</p>
<p><strong>Leis e decretos sobre acessibilidade:</strong></p>
<p>Decreto nº 5.296 de 02 de dezembro de 2004 (<a class="external-link" href="http://www.planalto.gov.br/ccivil_03/_Ato2004-2006/2004/Decreto/D5296.htm" target="_blank" title="">http://www.planalto.gov.br/ccivil_03/_Ato2004-2006/2004/Decreto/D5296.htm</a>).<br />Decreto nº 6.949, de 25 de agosto de 2009 - Promulga a Convenção Internacional sobre os Direitos das Pessoas com Deficiência e seu Protocolo Facultativo, assinados em Nova York, em 30 de março de 2007 (<a class="external-link" href="http://www.planalto.gov.br/ccivil_03/_ato2007-2010/2009/decreto/d6949.htm" target="_blank" title="">http://www.planalto.gov.br/ccivil_03/_ato2007-2010/2009/decreto/d6949.htm</a>).'<br />Decreto nº 7.724, de 16 de Maio de 2012 - Regulamenta a Lei No 12.527, que dispõe sobre o acesso a informações (<a class="external-link" href="http://www.planalto.gov.br/ccivil_03/_ato2011-2014/2012/Decreto/D7724.htm" target="_blank" title="">http://www.planalto.gov.br/ccivil_03/_ato2011-2014/2012/Decreto/D7724.htm</a>).<br />Modelo de Acessibilidade de Governo Eletrônico (<a class="external-link" href="http://www.governoeletronico.gov.br/acoes-e-projetos/e-MAG" target="_blank" title="">http://www.governoeletronico.gov.br/acoes-e-projetos/e-MAG</a>).<br />Portaria nº 03, de 07 de Maio de 2007 - formato .pdf (35,5Kb) - Institucionaliza o Modelo de Acessibilidade em Governo Eletrônico – e-MAG (<a class="external-link" href="http://www.governoeletronico.gov.br/biblioteca/arquivos/portaria-no-03-de-07-05-2007" target="_blank" title="">http://www.governoeletronico.gov.br/biblioteca/arquivos/portaria-no-03-de-07-05-2007</a>).<br />Decreto nº 13.146 de 06 julho de 2015. - Institui a Lei Brasileira de Inclusão da Pessoa com Deficiência (Estatuto da Pessoa com Deficiência) - Art. 63. É obrigatória a acessibilidade nos sítios da internet mantidos por empresas com sede ou representação comercial no País ou por órgãos de governo, para uso da pessoa com deficiência, garantindo-lhe acesso às informações disponíveis, conforme as melhores práticas e diretrizes de acessibilidade adotadas internacionalmente.</p></div>
	
